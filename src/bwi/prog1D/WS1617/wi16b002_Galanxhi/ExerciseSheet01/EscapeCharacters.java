package bwi.prog1D.WS1617.wi16b002_Galanxhi.ExerciseSheet01;


import bwi.prog.utils.TextIO;

public class EscapeCharacters {
	public static void main(String[] args){
		TextIO.putln("escape sequence is started with a backslash: \\");
		TextIO.putln("to print a tab (\t)	use \"\\t\"");
		TextIO.putln("to print a backslash (\\)	use \"\\\\\"");
		TextIO.putln("to print double quotes (\")	use \"\\\"\"");
		TextIO.putln("to print a single quote (')	use \"\\'\"");
		TextIO.putln("to start a new line (\n)				use \"\\n\"");

	}
}
